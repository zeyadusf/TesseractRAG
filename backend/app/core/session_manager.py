"""
session_manager.py
------------------
Core session management for TesseractRAG.

Contains:
    - SessionState  : In-memory representation of a single RAG session
    - SessionManager: Registry that creates, retrieves, lists, and deletes sessions

Storage backend: Cloudflare R2 (replaces local disk).
All persistence goes through R2Storage — no direct filesystem I/O here.
FAISS indexes are serialized to/from bytes via io.BytesIO for R2 transport.

Owner Identity (v1.1):
    Each session is tied to an owner_id — a random UUID generated by the
    browser on first visit and stored in localStorage. It is sent as the
    X-Owner-ID header on every request.

    This gives each browser a stable anonymous identity without requiring
    any login or database of users. Sessions created by owner A are
    invisible to owner B.

    Weakness: clearing localStorage loses the owner_id and therefore
    access to all owned sessions. Acceptable for a free-tier portfolio
    project. A future JWT auth layer would replace this.
"""

import io
import json
